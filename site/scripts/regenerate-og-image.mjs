import { existsSync, readFileSync, rmSync, writeFileSync } from 'node:fs';
import net from 'node:net';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { spawn, spawnSync } from 'node:child_process';

const scriptDir = dirname(fileURLToPath(import.meta.url));
const siteRoot = resolve(scriptDir, '..');
const outputPath = join(siteRoot, 'public', 'og.png');
const generatedPagePath = join(siteRoot, 'src', 'pages', 'og-image-preview.astro');
const generatedRouteDir = join(siteRoot, 'dist', 'og-image-preview');
const generatedPageSource = `---
import OgImage from '../templates/OgImage.astro';
---

<OgImage />
`;
const previewPath = '/og-image-preview/';
const width = 1200;
const height = 630;

const npm = process.platform === 'win32' ? 'npm.cmd' : 'npm';
const chrome = findChrome();

if (!chrome) {
  throw new Error('Could not find Chrome. Set CHROME_PATH to the Chrome or Chromium executable.');
}

if (existsSync(generatedPagePath) && readFileSync(generatedPagePath, 'utf8') !== generatedPageSource) {
  throw new Error(`${generatedPagePath} already exists and was not generated by this script.`);
}

writeFileSync(generatedPagePath, generatedPageSource);

let previewOutput = '';
let preview;

const stopPreview = () => {
  if (preview && !preview.killed) {
    preview.kill('SIGTERM');
  }
};

process.once('SIGINT', () => {
  stopPreview();
  process.exit(130);
});
process.once('SIGTERM', () => {
  stopPreview();
  process.exit(143);
});

try {
  await run(npm, ['run', 'build'], { cwd: siteRoot });

  const port = await getOpenPort();
  preview = spawn(
    npm,
    ['run', 'preview', '--', '--host', '127.0.0.1', '--port', String(port)],
    {
      cwd: siteRoot,
      env: { ...process.env, NO_COLOR: '1' },
      stdio: ['ignore', 'pipe', 'pipe'],
    },
  );

  preview.stdout.on('data', (chunk) => {
    previewOutput += chunk;
  });
  preview.stderr.on('data', (chunk) => {
    previewOutput += chunk;
  });

  const url = `http://127.0.0.1:${port}${previewPath}`;
  await waitForUrl(url, preview);

  const chromeArgs = [
    '--headless=new',
    '--disable-gpu',
    '--hide-scrollbars',
    '--force-device-scale-factor=1',
    `--window-size=${width},${height}`,
    '--virtual-time-budget=4000',
    `--screenshot=${outputPath}`,
    url,
  ];

  if (process.getuid?.() === 0) {
    chromeArgs.unshift('--no-sandbox');
  }

  await run(chrome, chromeArgs, { cwd: siteRoot });
  console.log(`Regenerated ${outputPath}`);
} catch (error) {
  if (previewOutput.trim()) {
    console.error(previewOutput.trim());
  }
  throw error;
} finally {
  stopPreview();
  rmSync(generatedPagePath, { force: true });
  rmSync(generatedRouteDir, { force: true, recursive: true });
}

function findChrome() {
  const candidates = [
    process.env.CHROME_PATH,
    '/Applications/Google Chrome.app/Contents/MacOS/Google Chrome',
    '/Applications/Chromium.app/Contents/MacOS/Chromium',
    '/usr/bin/google-chrome-stable',
    '/usr/bin/google-chrome',
    '/usr/bin/chromium',
    '/usr/bin/chromium-browser',
  ].filter(Boolean);

  for (const candidate of candidates) {
    if (existsSync(candidate)) {
      return candidate;
    }
  }

  for (const name of ['google-chrome-stable', 'google-chrome', 'chromium', 'chromium-browser']) {
    const resolved = commandPath(name);
    if (resolved) {
      return resolved;
    }
  }

  return null;
}

function commandPath(command) {
  const result = spawnSync(process.platform === 'win32' ? 'where' : 'which', [command]);

  if (result.status !== 0) {
    return null;
  }

  return result.stdout.toString().trim().split(/\r?\n/)[0] || null;
}

function getOpenPort() {
  return new Promise((resolvePort, reject) => {
    const server = net.createServer();
    server.once('error', reject);
    server.listen(0, '127.0.0.1', () => {
      const address = server.address();
      server.close(() => {
        if (address && typeof address === 'object') {
          resolvePort(address.port);
        } else {
          reject(new Error('Could not allocate a local preview port.'));
        }
      });
    });
  });
}

async function waitForUrl(url, child) {
  const deadline = Date.now() + 30000;

  while (Date.now() < deadline) {
    if (child.exitCode !== null) {
      throw new Error(`Astro preview exited before ${url} was available.`);
    }

    try {
      const response = await fetch(url);
      if (response.ok) {
        return;
      }
    } catch {
      // Preview is still starting.
    }

    await sleep(250);
  }

  throw new Error(`Timed out waiting for ${url}.`);
}

function run(command, args, options = {}) {
  return new Promise((resolveRun, reject) => {
    const child = spawn(command, args, {
      ...options,
      stdio: options.stdio ?? 'inherit',
    });

    child.once('error', reject);
    child.once('exit', (code, signal) => {
      if (code === 0) {
        resolveRun();
      } else {
        reject(new Error(`${command} ${args.join(' ')} failed with ${signal ?? `exit code ${code}`}.`));
      }
    });
  });
}

function sleep(ms) {
  return new Promise((resolveSleep) => setTimeout(resolveSleep, ms));
}
